const RTL_LOCALES = ['ar', 'he', 'fa', 'ur'];

// Detect locale from browser or stored preference
function detectLocale() {
  try {
    const stored = localStorage.getItem('app_locale');
    if (stored && translations[stored]) return stored;
    const browser = navigator.language?.split('-')[0];
    if (browser && translations[browser]) return browser;
  } catch (_) {}
  return 'en';
}

let currentLocale = detectLocale();

export function getLocale() {
  return currentLocale;
}

export function setLocale(locale) {
  if (!translations[locale]) {
    console.warn(`[i18n] Locale "${locale}" not supported, falling back to "en"`);
    locale = 'en';
  }
  currentLocale = locale;
  try {
    localStorage.setItem('app_locale', locale);
  } catch (_) {}

  // Apply RTL direction
  if (RTL_LOCALES.includes(locale)) {
    document.documentElement.setAttribute('dir', 'rtl');
    document.documentElement.setAttribute('lang', locale);
  } else {
    document.documentElement.setAttribute('dir', 'ltr');
    document.documentElement.setAttribute('lang', locale);
  }

  // Dispatch event so components can re-render
  window.dispatchEvent(new CustomEvent('localechange', { detail: { locale } }));
}

export function isRTL(locale = currentLocale) {
  return RTL_LOCALES.includes(locale);
}

export function t(key, fallback) {
  const dict = translations[currentLocale] || translations['en'];
  return dict[key] ?? translations['en'][key] ?? fallback ?? key;
}

